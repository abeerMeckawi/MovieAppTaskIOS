import Foundation

struct VideoDetails: Codable {
    let iso_639_1 : String?
    let iso_3166_1 : String?
    let name : String?
    let key : String?
    let published_at : String?
    let site : String?
    let size : Int?
    let type : String?
    let official : Bool?
    let id : String?

}
